# Tuple is Not Mutable
# A tuple is not mutable, meaning that once a tuple is created, its elements cannot be modified
# Tuples are defined by enclosing elements in parentheses ().
# Tuples are similar to lists, but they are immutable, meaning that their elements cannot be changed
# Tuples are used to store a collection of items that do not need to be changed.
# Tuples are faster and more memory-efficient than lists.
# Tuples are often used when a function needs to return multiple values.

